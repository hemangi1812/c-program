
#include<stdio.h>
int main()
{
    //print statement
    printf("hello world\n");
    //data type values
    int a=10;
    printf("value of a is %d",a);
    //float data type
    float b=10.5;
    printf("\nvalue of b is %f",b);
    //double data type
    double d=1111.11;
    printf("\nvalue of d is %lf",d);
    //character data type
    char c='A';
    printf("\nvalue of c is %c",c);

    return 0;
}